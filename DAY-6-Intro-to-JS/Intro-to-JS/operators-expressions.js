//Arithmetic operations
let x = 33;
let y = 15;

//get sum or total '+'
let sum = x + y;
console.log("Sum:", sum);


//get difference '-'
let difference = x - y;
console.log("Difference:", difference);

//get product '*'
let product = x * y;
console.log("Product:", product);

//get quotient '/'
let quotient = x / y;
console.log("Quotient:", quotient);

//get modulus '%'
let remainder = x % y;
console.log("Remainder:", remainder);


// Assignment expressions
let a = 10;
let b = 5;

//  Addition '=+'
a += b;
console.log("a += b is:", a);

// Substraction '-='

a -= b;
console.log("a -= b is:", a);

// Multiplication '*='

a *= b;
console.log("a *= b is:", a);

// Division '/='

a /= b;
console.log("a /= b is:", a);

